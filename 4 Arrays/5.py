# Declare an array with integer values 2, 3, 7, 5. In interactive mode, display:
integers = [2, 3, 7, 5]

# a. number of elements in the array
print(len(integers))

# b. second number in the array
print(integers[1])

# c. last but one number
print(integers[-2])

# d. sum of the first and last number
print(integers[0] + integers[-1])